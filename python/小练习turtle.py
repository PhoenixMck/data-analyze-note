from turtle import *
import time as t

setup(10000, 10000, 0, 0)
"""画三角形"""
color("black", "red")
pensize(5)
begin_fill()
circle(50, 360, 3)
# circle(-50,360,3)
penup()
setx(150)
pendown()
setheading(60)
circle(50, 360, 3)
end_fill()
penup()
goto(280, 40)  # 不同于set，就算penup了依旧会被填充
setheading(90)
pendown()
begin_fill()
circle(50, 360, 3)
penup()
end_fill()

home()
goto(-500, -150)
color("black", "blue")
pensize(5)
begin_fill()
circle(50, 360, 4)
end_fill()
penup()
forward(100)
pendown()
color("black", "green")
pensize(5)
begin_fill()
circle(50, 360, 5)
end_fill()
penup()
forward(100)
# setx(200) 这里的x是绝对坐标啊
pendown()
color("black", "purple")
pensize(5)
begin_fill()
circle(50, 360, 6)
end_fill()

for num in range(7, 13):
    penup()
    forward(100)
    pendown()
    begin_fill()
    circle(50, 360, num)
    end_fill()

t.sleep(2)
